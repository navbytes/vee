#!/usr/bin/env node
/**
 * Vee plugin bundler.
 *
 * Builds every sample plugin under `samples/<name>/` (identified by its
 * `vee.json` manifest) into a single self-contained IIFE bundle at
 * `dist/<manifest.id>.js`. Uses esbuild's CONTEXT API so watch mode does fast
 * incremental rebuilds (esbuild caches file contents + parsed ASTs between
 * builds).
 *
 *   node bundle.mjs            → build once, then watch for changes (dev loop)
 *   node bundle.mjs --once     → one incremental build, then exit (CI / fixtures)
 *
 * Build config rationale (see docs/ARCHITECTURE.md §4):
 *   - bundle:true               single file, inline everything
 *   - format:"iife"             no ESM/CJS module system at runtime (JSC has no
 *                               loader); the bundle just executes top-to-bottom
 *   - platform:"neutral"        no Node or browser globals assumed
 *   - target:"es2021"           a JavaScriptCore-safe baseline
 *   - sourcemap:true            JSC honors source maps for stack traces
 *   - ZERO externals            nothing may remain as a runtime import/require
 */

import * as esbuild from "esbuild";
import { readFile, readdir, mkdir, stat } from "node:fs/promises";
import { existsSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const SAMPLES_DIR = resolve(__dirname, "samples");
const DIST_DIR = resolve(__dirname, "dist");
const SDK_ENTRY = resolve(__dirname, "packages/sdk/src/index.ts");

const ONCE = process.argv.includes("--once");

/**
 * Discover buildable plugins: any `samples/<name>/` dir with a `vee.json`
 * manifest and an `src/index.ts` entry. Returns the esbuild entry + output path.
 */
async function discoverPlugins() {
  if (!existsSync(SAMPLES_DIR)) return [];
  const entries = await readdir(SAMPLES_DIR, { withFileTypes: true });
  const plugins = [];
  for (const e of entries) {
    if (!e.isDirectory()) continue;
    const dir = join(SAMPLES_DIR, e.name);
    const manifestPath = join(dir, "vee.json");
    const entryPath = join(dir, "src", "index.ts");
    if (!existsSync(manifestPath) || !existsSync(entryPath)) continue;
    const manifest = JSON.parse(await readFile(manifestPath, "utf8"));
    if (!manifest.id) {
      throw new Error(`vee.json in ${dir} is missing an "id"`);
    }
    plugins.push({
      id: manifest.id,
      name: manifest.name ?? e.name,
      entry: entryPath,
      outfile: join(DIST_DIR, `${manifest.id}.js`),
    });
  }
  return plugins;
}

/**
 * Map `@vee/sdk` (and its subpath imports) to the SDK source so esbuild inlines
 * it. This is what keeps the output free of any runtime import of the SDK.
 */
function sdkAliasPlugin() {
  return {
    name: "vee-sdk-alias",
    setup(build) {
      build.onResolve({ filter: /^@vee\/sdk$/ }, () => ({ path: SDK_ENTRY }));
    },
  };
}

/** Shared esbuild options for a single plugin bundle. */
function buildOptions(plugin) {
  return {
    entryPoints: [plugin.entry],
    outfile: plugin.outfile,
    bundle: true,
    format: "iife",
    platform: "neutral",
    target: ["es2021"],
    sourcemap: true,
    // Hard guarantee: nothing is left external. If a bundle tries to import a
    // bare module we can't resolve, fail the build rather than emit a require().
    external: [],
    legalComments: "none",
    logLevel: "info",
    plugins: [sdkAliasPlugin()],
    // A small banner documents provenance in the emitted artifact.
    banner: {
      js: `// Vee plugin bundle — ${plugin.id} — generated by bundle.mjs (do not edit)`,
    },
  };
}

async function run() {
  await mkdir(DIST_DIR, { recursive: true });
  const plugins = await discoverPlugins();
  if (plugins.length === 0) {
    console.warn("bundle.mjs: no plugins found under samples/*/vee.json");
    return;
  }

  // One esbuild context per plugin (independent incremental caches).
  const contexts = [];
  for (const plugin of plugins) {
    const ctx = await esbuild.context(buildOptions(plugin));
    contexts.push({ plugin, ctx });
  }

  if (ONCE) {
    // Single incremental build for each, then dispose and exit.
    for (const { plugin, ctx } of contexts) {
      await ctx.rebuild();
      const { size } = await stat(plugin.outfile);
      console.log(`built ${plugin.id} → dist/${plugin.id}.js (${size} bytes)`);
      await ctx.dispose();
    }
    console.log(`bundle.mjs: built ${contexts.length} plugin(s).`);
    return;
  }

  // Watch mode: prime once so dist is populated immediately, then watch.
  for (const { plugin, ctx } of contexts) {
    await ctx.rebuild();
    const { size } = await stat(plugin.outfile);
    console.log(`built ${plugin.id} → dist/${plugin.id}.js (${size} bytes)`);
    await ctx.watch();
  }
  console.log(`bundle.mjs: watching ${contexts.length} plugin(s) for changes…`);
  // Keep the process alive while watching.
  await new Promise(() => {});
}

run().catch((err) => {
  console.error("bundle.mjs failed:", err);
  process.exitCode = 1;
});
